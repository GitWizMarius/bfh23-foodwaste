import datetime

#Feiertage Datenrahmen 2020 bis 2023 as YYYY-MM-DD
Feiertage=["2020-01-01","2020-01-06","2020-04-10","2020-04-12","2020-04-13","2020-05-01","2020-05-21","2020-06-01","2020-06-11","2020-10-03","2020-11-01","2020-12-25","2020-12-26",
           "2021-01-01","2021-01-06","2021-04-02","2021-04-05","2021-01-01","2021-05-01","2021-05-13","2021-05-24","2021-06-03","2021-10-03","2021-01-01","2021-11-01","2021-12-25","2021-12-26",
           "2022-01-01","2022-01-06","2022-04-15","2022-04-18","2022-05-01","2022-05-26","2022-06-06","2022-06-16","2022-10-03","2022-11-01","2022-12-25","2022-12-26"]

def is_werktag(day, month, year):
    # Create a datetime object for the given date
    date = datetime.date(year, month, day)
    if str(date) in Feiertage:
        return False
    
    return date.weekday() != 6